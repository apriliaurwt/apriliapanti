        <p style="text-align:justify;text-indent:0.5in;">
            Berkat partisipasi masyarakat sejarah panti asuhan yatim-piatu AR-rodiyah (PA AR-rodiyah) diawali dengan keprihatinan
            terhadap banyaknya anak-anak yatim di daerah itu. Saat itu sebuah majelis Ta’lim yang diberi nama Al jihad  melakukan
            kerja –kerja sosialnya dengan menyantuni anak-anak yatim di kelurahan sambiroto, kecamatan tembalang –semarang
            penyantunan ini dilakukan setiap tanggal 10 Muharam dari tahun 1989 hingga akhirnya berkembang menjadi menyantuni
            keluarga yang kurang mampu . Aktifitas ini berjalan sampai tahun 1993. Melihat semakin tahun semakin banyak yang
            harus disantuni, maka pada tahun 1994, majelis bersama masyarakat kelurahan sambiroto-semarang mendirikan lembaga
            social panti asuhan dengan maksud agar pendidikan dan kesejahteraan anak  yatim lebih terjamin di masa datang.
            Sekretariat lembaga sosial ini kemudian menjadi panti asuhan AR-rodiyah berada di jl. Sambiroto XII semarang.
            <br />
            <p style="text-align:justify;text-indent:0.5in;">
            Pada bulan ramadhan  1995, atas kesepakatan bersama masyarakat, maka di drikan secara resmi PA AR-rodiyah
            di bawah naungan yayasan al-huda . pendirian remi ini ditandai dengan gedung pertama seluas 7X8 meter ,
            yang belakangan di gunakan asrama perempuan. Pembangunan pertama merupakan swadaya murni masyarakat muslim
            sekitar masyarakat semarang. Pada perjalananya, berdasarkan catatan notariat pada tahun 1996,
            Yayasan al-huda berfungsi sebagai lembaga yang menangani program panti asuhan lembaga pedidikan dan majelis Ta’lim.
            </br />
            <p style="text-align:justify;text-indent:0.5in;">
            Lambat namun pasti, realisasi perencanaan pembangunan pondok panti terus di upayakan denagn cara menampung zakat mal,
            zakat fitrah, sumbangan maupun sedekah dari masyarakat kota Semarang. Baru pada tahun 1997, pembangunan pondok panti
            berikut kantor dan dapur dimulai. Pembangunan yang pendanaanya dari masyarakat muslim di kota semarang dan sekitarnya ,
            ini dibangun atas dukungan tenaga(buruh dan tukang ) masyarakat sekitar panti . walau pun yang di bangun hanya asrama putra,
            dapur dan perkantoran yang tidak begitu luas,  akan tetapi pembanguna pondok  baru dapat rampung seluruhnya pada tahun 1999.
            <br />
            <p style="text-align:justify;text-indent:0.5in;">
            Saat ini, walau tak begitu megah , setidaknya fasilitas panti berupa asrama putra-putri , ruang aula, kantor, gudang beras,
            kandang rusa dan ruang bermain sudah tersedia. Dengan fasilitas ini, panti dapat memberikan layanan kepada  sebanyak 61 orang anak
            dan 70 orang non panti. Salah satu bentuk   layanan yang sangat membekas di hati anak adalah operasi pembuatan lubang dubur dan
            vagina anak panti barnama Tri Wahyu Ningsih.<br />
            <p style="text-align:justify;text-indent:0.5in;">
            Berdiri kokohnya PA AR-rodiyah saat ini berkat partisipasinya masyarakat kota semarang, khususnya masyarakat keluraham sambiroto
            yang sejak lama menginginkan berdirinya panti asuhan untuk anak yatim-piatu. Partisipasi anak panti dan masyarakat keterbatasan
            tenaga pengelolah panti yang selama ini bekerja dengan system relawan, mendorong pengurus panti mengembangkan partisipasi anak
            panti dan masyarakat  untuk mengelola dan merawat panti secara keseluruhan. Anak-anak panti yang sudah alumni maupun yang duduk
            di bangku sederajat sekolah lanjutan tingkat atas diajak menjadi pengelola panti. Proses pelayanan panti secara keseluruhan ada
            di tangan anak-anak panti yang memang tinggal di lingkungan panti. Proses ini dilakkan secara partisipatip dimana semua aturan–aturan
            yang selama ini di berlakukan dipanti lahir dari hasil sebuah musyawarah anak panti yang kemudian dikonsolidasikan dengan pengurus
            panti. Berjalannya proses ini bukanlah tanpa sebab yang berarti, mengingat kegagalan yang pernah di alami pengurus, terutama mencoba
            mengelola sendiri usaha koperasi tanpa melibatkan anak panti. <br />
            <p style="text-align:justify;text-indent:0.5in;">
            Selain itu, pengurus   panti melihat bahwa dengan keterbatasan tenaga pengurus dan relawan (baik masyarakat maupun dari mahasiswa)
            rasanya panti tidak dapat dikelola dengan baik. Itu sebabnya, sejak tahun 2001 seluruh anak panti di ajak untuk mempunyai rasa
            memiliki (sence of   belonging) terhadapa keberadaan dan keberlanjutan  panti. Partisipasi ini tidak hanya berkembang dalam rangka
            menjaga keindahan, kebersihan dan ketertiban lingkungan panti, melainkan juga sampai  kepada partisipasi dalam mengelola setiap usaha
            ekonomi produktip (UEP) panti.<br />
            <p style="text-align:justify;text-indent:0.5in;">
            Adanya saling memiliki , (sence of  belonging ), adanya usaha ekonomi yang memberi intensif bagi anak panti dan adanya kesetiakawanan
            yang tersosialisasi jauh-jauh hari di panti menjadikan pengelola  KH. Ahmad suhari menjadi lebih ringan dan berkah. Kemudahan ini
            semakin terasa bahwa manakala panti memutuskan bahwa utuk tugas HUMAS diserahkan sepenuhnya kepada masyarakat di lingkungan panti
            sebanyak 4 orang terdiri dari bapak soadi , Agus Cahyono, Yoni dan Kasino. Dari keempat inilah terjaminya keberadaan panti di masyarakat,
            begitu pula keamanan panti semakin terjaga dengan baik. Usaha pendukung keberlanjutan panti mengandalkan sumbangan dan zakat masyarakat
            untuk menjaga keberlangsungan hidup panti  sangatlah riskan. Selain tidak dapat diprediksi  jumlah sumbangan dan zakat yang akan terima,
            juga tak di ketahui  kapan  dukungan dana seperti ini akan masuk kas panti. Kalau pun dapat di prediksi waktu datangnya sumbangan seperti
            pada bulan ramadhan (melalui zakat fitrah dan mal ), namun jumlahnya tidaklah mungkn di prediksi. <br />
            <p style="text-align:justify;text-indent:0.5in;">
            Selain alasan-alasan ini, pengurus panti yang selama ini bekerja suka relawan, harus juga dipikirkan kesejahteraanya, setidaknya
            untuk insentif bulanan mengingat panti tidak menerapkan sitem gaji. Berdasarkan alasan di atas , pengurus panti kemudian membuat
            usaha-usaha ekonomi produktif. Ada 3 kategori bentuk kerja sama usaha produktip panti. Pertama , UEP bekerja sama dengan anak panti ;
            kedua, ekonomi produktif bekerja sama dengan masyarakat ; ketiga, usaha yang di kelola bekerjasama dengan pengusaha.
            kerja sama anak panti juga di maksudkan pula  sebagai proses belajar anak agar menjadi lebih mandiri dan mulai memikirkan masa depanya kelak.
            <br />
            <p style="text-align:justify;text-indent:0.5in;">
            Saham terbesar anak panti di tambah pengurus panti. Bentuk koperasi dengan saham bersama ini mulai dilaksanakan pada tahun 2001.
            Sebelum tahun 2001, koperasi yang berdiri tahun 1999 ini, pernah bangkrut karena system pengelolaanya sepenuhnya di kelola oleh
            pengurus panti. Bangkrut koperasi waktu itu lebih disebabkan tidak terlibatnya anak panti dalam pengelolaan .
            karena tidak adanya rasa memiliki dari anak panti koperasitidak yang di peroleh tidak memadai untuk koperasi terus di pertahankan.<br />
            <p style="text-align:justify;text-indent:0.5in;">
            Usaha produktif lain yang di kelola oleh anak panti adalah penjualan air minum ke msyarakat, terutama warung- warung yang ada di lngkungan panti.
            Usaha ini, sepenuh nya di kelola oloeh anak panti , dimana yang terlibat mendapat intensip dari penjualan air satu jerigen Rp700.
            Begitu jutga usaha roti yang di kelola anak panti terutama anak perempuan. Usaha musiman ini , di lakukan saat lebaran  atau hari
            besar  lainnya sesuai permintaan konsumen yang  berasak dari instansi pemerintahan dan kepolisian  di semarang. luasnya jangkauan
            pemasaran kue dan roti ini berkat sosialiasasi kanwil sosisal jateng . selain itu , jenis usaha yang sedang di kembangan adalah
            peternakan kijang, kambing, ayam, kolam, (gurami  dan tawes ) dan pertanian sayuran.<br />
